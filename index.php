  			<h3 class="title center-align"> The Itamae </h3>
  			<p>
	  			At the age of 18, Johnny Chang co-opened a Japanese restaurant in Taiwan. Now as owner and senior itamae-san (sushi chef) of Yoshio Sushi, formerly known as Taki Sushi, he brings traditional Japanese cuisine and izakaya sushi bar experience to his patrons. Johnny transformed Taki Sushi, which was originally co-owned by one of the founding fathers of the California Roll, to Yoshio Sushi in early 2016. Named after Johnny's father, Yoshio, he pays homage and brings to life his father's experiences and his true passions of appreciating Japanese cuisine. 
